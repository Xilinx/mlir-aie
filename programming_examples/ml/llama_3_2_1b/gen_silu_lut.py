"""Codegen: emit a static const bf16[256] SiLU LUT header.

Used to make llama_silu_mul_int8.cc bit-exact against the numpy
reference: both compute silu(i * gate_scale) for i in int8, and look
up the same bf16 entry. The output header is included by the kernel
and the same LUT (recomputed in numpy with the same arithmetic) is
the canonical reference in test_silu_mul_int8.py.
"""

import argparse
import sys

import numpy as np
from ml_dtypes import bfloat16


def silu_lut(gate_scale: float) -> np.ndarray:
    """256 bf16 entries: lut[i + 128] = silu(i * gate_scale) for i in int8."""
    out = np.zeros(256, dtype=bfloat16)
    for raw in range(256):
        i = raw - 128
        x = float(i) * gate_scale
        silu = x / (1.0 + np.exp(-x))
        out[raw] = bfloat16(silu)
    return out


def main():
    p = argparse.ArgumentParser()
    p.add_argument("--gate-scale", type=float, required=True)
    p.add_argument("-o", required=True)
    args = p.parse_args(sys.argv[1:])

    lut = silu_lut(args.gate_scale)
    raw = lut.view(np.uint16)

    with open(args.o, "w") as f:
        f.write("// auto-generated by gen_silu_lut.py; do not edit\n")
        f.write(f"// gate_scale = {args.gate_scale}\n")
        f.write("#pragma once\n")
        f.write("#include <stdint.h>\n\n")
        f.write("static const uint16_t llama_silu_lut_raw[256] = {\n")
        for i in range(0, 256, 8):
            row = ", ".join(f"0x{raw[j]:04x}" for j in range(i, i + 8))
            f.write(f"    {row},\n")
        f.write("};\n")


if __name__ == "__main__":
    main()
